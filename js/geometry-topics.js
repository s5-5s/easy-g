const TOPICS_BY_ID = new Map(TOPICS.map((topicObject) => [topicObject.id, topicObject]));

/**
 * @returns {ReadonlyArray<object>}
 */
export function getAllTopics() {
    return TOPICS;
}

/**
 * @param {string | undefined} topicIdentifier
 * @returns {object | undefined}
 */
export function getTopicById(topicIdentifier) {
    if (typeof topicIdentifier !== "string") {
        return TOPICS[0];
    }

    return TOPICS_BY_ID.get(topicIdentifier) ?? TOPICS[0];
}
